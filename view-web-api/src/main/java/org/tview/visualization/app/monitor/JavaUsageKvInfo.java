package org.tview.visualization.app.monitor;

public class JavaUsageKvInfo {
    private String name;
    private String keyProperty;
    private JavaUsageEntity javaUsageEntity;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getKeyProperty() {
        return keyProperty;
    }

    public void setKeyProperty(String keyProperty) {
        this.keyProperty = keyProperty;
    }

    public JavaUsageEntity getJavaUsageEntity() {
        return javaUsageEntity;
    }

    public void setJavaUsageEntity(JavaUsageEntity javaUsageEntity) {
        this.javaUsageEntity = javaUsageEntity;
    }
}
